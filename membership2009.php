                <font color="#333333"
                   size="2"
                   face="Arial, Helvetica, sans-serif"><b>Share
                   Purchase</b></font>
              </p>
              <p>
                <font color="#333333"
                   size="2"
                   face="Arial, Helvetica, sans-serif">The
                   Virginia Hills Club, Inc. is owned by its
                   members, who are the shareholders in the Club.
                   To become a member, either a family or single,
                   pay a one-time fee to purchase a share. That
                   fee is <b>$500</b> for the 2009 season. New
                   shareholders can pay this amount all at once
                   or make a <b>$50</b> down payment when they
                   join, with the rest paid in increments through
                   the Club's easy payment plan. Payment can be
                   made by check or credit card via
                   Paypal.</font>
              </p>
              <p>
                <font color="#333333"
                   size="2"
                   face=
                   "Arial, Helvetica, sans-serif">Shareholders
                   who terminate their membership by informing
                   the Club in writing and requesting a buy-back
                   are eligible to have 85% of their share price
                   returned to them at a time approved by the
                   Club's Board.</font>
              </p>
              <p>
                <font color="#333333"
                   size="2"
                   face=
                   "Arial, Helvetica, sans-serif"><b>Seasonal
                   Dues</b></font>
              </p>
              <p>
                <font color="#333333"
                   size="2"
                   face=
                   "Arial, Helvetica, sans-serif">Shareholders
                   also pay dues before the start of every
                   season, which should be paid by May 1 every
                   year. The fee for family membership dues in
                   2009 is <strong>$525</strong>. The fee for
                   single membership dues in 2009 is
                   <strong>$280</strong>. Payments may be spaced
                   out in advance of that date. New members
                   should also pay seasonal dues by May 1 or as
                   soon as they join. New members whose
                   households include extended family members
                   should discuss special arrangements with the
                   Membership Committee representatives.</font>
              </p>
              <p>
                <font color="#333333"
                   size="2"
                   face="Arial, Helvetica, sans-serif">Two
                   classes of associate (non-shareholding)
                   memberships are available: associate and
                   military. Associate Members pay a
                   single-season fee of <b>$760</b> (no share
                   purchase). If you anticipate being in the area
                   or using the pool for more than one season,
                   however, it makes sense to become a
                   shareholder from the beginning. Associate
                   membership fees are likely to increase in
                   future seasons.</font>
              </p>
              <p>
                <font color="#333333"
                   size="2"
                   face="Arial, Helvetica, sans-serif"><b>For
                   Military Personnel</b></font>
              </p>
              <p>
                <font color="#333333"
                   size="2"
                   face="Arial, Helvetica, sans-serif">The
                   Virginia Hills Club also welcomes military
                   families, who only may be in our community for
                   a few years. We have created a special type of
                   Associate Membership, the Active Duty Military
                   Membership, which costs $<b>525</b> per season
                   in 2009 (no share purchase). This benefit can
                   be used for two consecutive seasons. If a duty
                   tour is extended or is expected to last for
                   more than two swim seasons, active duty
                   personnel are encouraged to consider becoming
                   shareholders. Special consideration will be
                   given when such military shareholders rotate
                   out of the area.</font>
              </p>
