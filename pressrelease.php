          <p>
            Virginia Hills Club, Inc., a swim club serving the
            Alexandria suburban community for over 50 years, has
            completed major renovations on its pool property,
            located in the Virginia Hills section of Fairfax
            County, Virginia. The completed renovations have
            increased the total square footage of the main pool
            and baby pool by over 35% to 4500 sq. ft.
            Improvements to the pool infrastructure and design
            feature the following:
          </p>
          <ul>
            <li>A new baby pool with a beach entry and a fountain
            in the center
            </li>
            <li>A larger shallow area in the main pool with an
            in-water bench
            </li>
            <li>A new diving well and half-meter diving board
            </li>
            <li>A dedicated area for the pool slide
            </li>
            <li>An upgraded filtration system
            </li>
            <li>Upgraded in-pool lighting
            </li>
            <li>A new white coat on the shell and all new coping
            stones around the pool edge
            </li>
            <li>Replacement of the deck between the pool and the
            fence adjacent to Lee District Park
            </li>
            <li>Compliance with 2009 pool drain safety
            regulations
            </li>
          </ul>
          <p>
            Virginia Hills Club was formed in 1955 and has been a
            fixture in the local community ever since. The new
            renovations were undertaken to serve a membership
            base which has been on the rise since 2004, when the
            Club adopted a long-term plan to improve its
            facilities and increase its membership. To kick off
            the second phase of its long term plan, Virginia
