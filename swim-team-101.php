          <h3>
            <a name="ch8"
                id="ch8"></a>Chapter 8: We'd Rather Have Fun Than
                Be in Division I
          </h3>
          <p>
            If you get to know the parents of swimmers in
            Division I teams, some of them will tell you that
            swimming isn't always fun. At the pinnacle of NVSL
            are the six best teams and they all want to be #1. At
            Virginia Hills, we like to win, but it isn't our
            reason for being. Some of the things we do to have
            fun are:
          </p>
          <p>
            <strong>Pep rallies:</strong> Pep rallies are held
            Friday nights before home meets at the pool. Coaches
            will lead swimmers in preparing the pool for the next
            morning's meet: learning cheers, making posters, and
            setting up equipment. It's also an opportunity for
            coaches and the team rep to share news.
          </p>
          <p>
            <strong>Team pictures:</strong> We take a team
            picture every year and we'd like your swimmer to be
            in it. You are not obligated to purchase a picture.
          </p>
          <p>
            <strong>Other social events:</strong> Over the course
            of the season we'll have a spaghetti dinner, a couple
            of ice cream socials, a trip to a nearby amusement
            park and other fun events.
          </p>
          <p>
            <strong>Swim banquet:</strong> We cap off the season
            with a swim banquet, which is a pot luck dinner held
            at the pool the evening of divisionals. We give out
            awards for season accomplishments and end the evening
            with family relays and team presentations.
          </p>
          <p>
            <strong>What's mandatory?</strong> While we hope you
            will participate in all the social activities, they
            are all optional. Similarly, the purchase of team
            suits and other team clothing items is entirely
            optional. What is mandatory is that you honor your
            commitments and have fun!
          </p>
