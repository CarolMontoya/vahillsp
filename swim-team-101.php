          <h3>
            <a name="ch3"
                id="ch3"></a>Chapter 3: Organizational Stuff
          </h3>
          <p>
            Let's cover a few organizational items, just to make
            sure we're in sync.
          </p>
          <h5>
            Team Reps and Coaches
          </h5>
          <p>
            The people you'll run into the most in your swim team
            dealings are the Team Reps and the Coaches. The Team
            Reps are volunteer parents who were new to swimming
            at one time just like you are today. The Team Reps
            are responsible for running every aspect of the swim
            team and are the representatives of the Virginia
            Hills team to other swim teams and the NVSL. It's a
            job that is impossible to do successfully without
            help from a great many parents. The Team Reps hire
            the Coaches who are responsible to the team reps for
            the swimming portion of the swim team program.
          </p>
          <h5>
            Northern Virginia Swimming League (NVSL)
          </h5>
          <p>
            In 1956, 8 Northern Virginia Pools founded the
            Northern Virginia Swim League. Today, the NVSL has
            over 10,000 swimmers on 104 teams and is the largest
            summer swim league in the United States. Obviously,
            with this many teams, there has to be some division
            of teams. During the off season, the NVSL ranks each
            team from 1 to 104 based primarily on swimmers times,
            and then divides the teams, based on these rankings,
            into about 18 Divisions of five or six teams. This
            means that the fastest teams are in the lower
            numbered Divisions and the less competitive teams are
            in the higher numbered Divisions. The other teams in
            our division, and meet locations, are on the NVSL's
            Web site, <a href=
            "http://nvsl.nvblu.com/plasma/core/site/nvsl">http://nvsl.nvblu.com/plasma/core/site/nvsl</a>.
            Directions to pools are provided prior to the meet.
          </p>
          <h5>
            Dual Meets
          </h5>
          <p>
            The six teams in each division swim the other five
            teams, one at a time on five consecutive Saturdays,
            in a series of Dual Meets ("A" Meets), so called
            because there are two teams competing. Based upon the
            results of these five meets, a division champion will
            be named.
          </p>
          <h5>
            Relay Carnivals
          </h5>
          <p>
            Another NVSL event is the Division Relay Carnival,
            which takes place on the Wednesday between the third
            and fourth weeks of the season. All six teams in each
            division converge on one pool for an evening of relay
            races. These include both Freestyle relays (each
            swimmer swims the Freestyle) and Medley relays (each
            swimmer swims a different stroke). The next night,
            all the Division Coordinators meet and relay teams
            are selected to swim at the All-Star Relay Carnival
            the following week. The sole criterion for selection
            to the All-Star Relay Carnival is to have one of the
            eighteen fastest times in events swum in the Division
            Relay Carnivals.
          </p>
          <h5>
            Divisionals
          </h5>
          <p>
            The sixth week, each Division has an Individual
            Championship meet, commonly referred to as
            "Divisionals." Each team is allowed to enter two
            swimmers in each event and a swimmer can enter no
            more than two events. If a team does not have two
            swimmers for an event, the other teams can bid in
            other swimmers to fill the empty lanes. This is an
            individual meet and is not scored.
          </p>
          <h5>
            All Stars
          </h5>
          <p>
            After the Divisionals, all the Division coordinators
            meet to select swimmers for the All- Stars meet the
            following week. The sole criterion for selection to
            All-Stars is to have one of the eighteen fastest
            times swum that day in an event in the Divisional
            meet. All Stars can be overwhelming for a first time
            swimmer as approximately 600 swimmers plus parents,
            coaches, and officials converge on a pool for a meet
            that takes about six hours. If your swimmer is fast
            enough to be named an All Star, it is an thrill they
            will never forget.
          </p>
