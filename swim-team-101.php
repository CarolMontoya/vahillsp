            <a name="ch2"
                id="ch2"></a>Chapter 2: Important Basic Stuff
          </h3>
          <h5>
            Practices
          </h5>
          <p>
            Practice times are posted on the Swim Team Bulletin
            Board and in the summer swim season section of our
            web site. Swimmers should come to practice regularly
            and be prepared to do their best. It's important to
            attend morning practice sessions if at all possible.
            Afternoon sessions compete with club members' use of
            the pool, are crowded, and are not offered each day.
            Please work with your friends and neighbors to
            arrange car pools to help swimmers attend morning
            practice.
          </p>
          <h5>
            Swimmer Apparel
          </h5>
          <p>
            Swimmers should wear a swimsuit that will be
            comfortable for racing. The Tidal Wave, like every
            other swim team, has a team suit. The wearing of the
            team suit is optional. There is a culture shock for
            some boys to the coverage of jammers. Usually, when
            they realize that this type of suit is good for
            racing and everyone else is wearing one, their
            opposition breaks down. Although a team suit is not
            required, suits or caps with any logo other than
            Tidal Wave are not permitted (wearing a plain
            suit/cap is fine). Caps are not required, but
            recommended. Team caps can be purchased from the team
            rep. Most swimmers use goggles.
          </p>
          <h5>
            Accessories for the well-dressed swimmer
          </h5>
          <p>
            Every swimmer needs a towel, the bigger and thicker
            the better. Other accessories you should consider are
            goggles, a sweat suit (for cool days), a swim cap
            (especially for girls with long hair), a hat or other
            sun protection, flip-flops or some other sort of
            footwear, and a bag to carry everything. You should
            also have liquid refreshment (Gatorade, All Sport, or
            a water bottle) and a light snack for during the meet
            such as fruit, granola bars or other healthy foods.
            Save the sweets at the concession stand as a reward
            for swimming a great swim.
          </p>
          <h5>
            Buddy Program
          </h5>
          <p>
            Being a new kid on the swim team can be a bewildering
            experience. We've instituted a program to give every
            swimmer a Buddy to cheer them on at swim meets and
            help them adjust to their new experience.
          </p>
