          <h3>
            <a name="ch7"
                id="ch7"></a>Chapter 7: What Do You Mean, My Kid
                DQ'd
          </h3>
          <p>
            In swimming, the rules must be followed in total or a
            disqualification, or DQ,is committed. This can be
            traumatic the first time a swimmer is DQ'd for just
            one mistake, but it isn't fair to other swimmers who
            swim the entire race per the rules to do otherwise.
          </p>
          <h5>
            What is a DQ?
          </h5>
          <p>
            A DQ (short for disqualification) is any violation of
            the rules observed by any appropriate official. Some
            of the more common reasons for DQing are as follows.
          </p>
          <p>
            Freestyle:
          </p>
          <ul>
            <li>Failure to touch the wall at the turning end of
            the pool
            </li>
            <li>Walking on the bottom or pulling on the lane
            lines
            </li>
            <li>Exiting the pool before swimming the specified
            distance
            </li>
          </ul>
          <p>
            Backstroke:
          </p>
          <ul>
            <li>Past vertical towards the breast at any time
            except during a flip turn
            </li>
            <li>Leaving the wall after a turn past vertical
            towards the breast
            </li>
            <li>Improper flip turn (older swimmers)
            </li>
          </ul>
          <p>
            <br>
            Breaststroke:
          </p>
          <ul>
            <li>Incorrect kick, such as a Scissors kick or
            Flutter kick
            </li>
            <li>Non-Simultaneous two-hand touch or one hand touch
            at turn or finish
            </li>
            <li>Toes not pointed outward during the propulsive
            part of the kick
            </li>
            <li>More than one stroke underwater with arms fully
            extended at start or turn
            </li>
            <li>Arm recovery past waist except on first stroke
            after start or turn
            </li>
            <li>Head didn't break surface by conclusion of second
            arm pull underwater after a start or turn
            </li>
          </ul>
          <p>
            Butterfly:
          </p>
          <ul>
            <li>Non-Simultaneous or one handed wall touch at the
            turn or finish
            </li>
            <li>Non-Simultaneous leg movement during kicks
            </li>
            <li>Arms don't break water surface during recovery
            (judged at the elbows)
            </li>
            <li>Non-Simultaneous arm movement during recovery
            </li>
          </ul>
          <p>
            Relay Races:
          </p>
          <ul>
            <li>A swimmer leaves the deck before the previous
            swimmer touches the wall or deck
            </li>
          </ul>
          <p>
            False Start:
          </p>
          <ul>
            <li>A swimmer starts the race early (more details
            <a href="#false">below</a>)
            </li>
          </ul>
          <h5>
            How will I know a DQ occurred?
          </h5>
          <p>
            Unlike football, we don't blow a whistle and announce
            to the world that a rules violation occurred. When a
            Stroke and Turn Judge observes a violation, he raises
            his hand to signify that he has observed a violation
            then writes it up on a DQ slip. The judge then takes
            the slip to the referee, who verifies that rule has
            been broken and can question the stroke and turn
            judge to ensure that he was able to see the violation
            that was cited. The referee then gives one copy of
            the DQ slip to the Team Rep and another copy to the
            Table Workers. Another clue that a DQ has occurred is
            a Stroke and Turn Judge writing and a longer than
            normal pause between events.
          </p>
          <p>
            Disqualifications for early relay takeoffs are done
            slightly differently. The referee receives all the
            take-off slips from all the judges. If both judges on
            a lane agree that an early takeoff occurred, the
            Referee will write the DQ slip to be distributed.
          </p>
          <h5>
            False Starts
          </h5>
          <p>
            A false start occurs whenever a swimmer moves towards
            the pool after having assumed a still position
            (taking his/her mark) and before the Starter has
            started the race. When this occurs, a swimmer is
            usually trying to anticipate the starting signal and
            beat the other swimmers into the water.
          </p>
          <p>
            If the false start is detected before the starting
            signal is sounded, the offending swimmer can be
            removed from the race prior to it starting. If a
            false start occurs but the starting signal has
            sounded, the race will not be stopped. Instead the
            false starting swimmer(s) will be notified of their
            false start at the conclusion of the race. The use of
            a recall signal is now limited to a bad start (i.e.
            not all swimmers were ready) or for a safety reason.
            If the starter sounds the recall signal, no swimmer
            can be removed for a false start.
          </p>
          <h5>
            How do I know if My Kid DQ'd
          </h5>
          <p>
            Your kid will probably know before you do since the
            Team Rep tells the coach, who tells the swimmer.
            You'll probably find out if you saw your swimmer
            finish with one of the top three times but he isn't
            announced later in the top three places. Similarly,
            someone else DQd if your swimmer finished in
            4<sup>th</sup>, 5<sup>th</sup> or 6<sup>th</sup>, yet
            is announced as one of the top three finishers.
            Another way to find out is by reading the official
            Meet Results, which are posted at the pool by 4 PM
            the day of the meet. You can also get results by
            visiting the <a href="http://nvsl.nvblu.com/">NVSL
            web site</a> or by reading the Fairfax Journal.
          </p>
          <h5>
            A Word About Officials and DQs
          </h5>
          <p>
            Every Official on the deck will always give the
            benefit of the doubt to the swimmer. Although the
            difference between legal but ugly vs illegal is
            sometimes close to call, any violation called by an
            official is an "I saw" not an "I think I saw".
          </p>
          <h5>
            Protesting Disqualifications
          </h5>
          <p>
            The Team Rep is the only person who can officially
            question a disqualification or any other call by an
            official. If something happens involving your
            swimmer, which you do not think is right, talk to the
            coach or the Team Rep. The Team Rep will initiate
            action in accordance with NVSL rules if thought to be
            appropriate.
          </p>
