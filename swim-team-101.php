          <h3>
            <a name="ch4"
                id="ch4"></a>Chapter 4: My Kid Says He's Supposed
                to Swim Like a Butterfly
          </h3>
          <p>
            If you're not a former swimmer, the strokes and their
            rules can be a cause of bewilderment. While the
            stroke rules are simple enough for a six year old to
            understand, most people do not have a copy of the US
            Swimming Rules, so we'll briefly describe the strokes
            below. The rules below are the US Swimming rules as
            modified for use in the NVSL. Teams in other leagues
            may have slightly different rules.
          </p>
          <h5>
            Freestyle
          </h5>
          <p>
            The freestyle is defined as any means of swimming
            across the pool. Any stroke and kick are acceptable.
            There are, however, a few don'ts associated with this
            stroke, specifically: (1) You cannot walk on the
            bottom or pull yourself along using the lane lines
            and (2) In a 50 Meter race (two pool lengths) you
            must touch the wall at the 25 meter end before
            touching the wall at the 50 meter end (This may seem
            obvious, but sometimes swimmers miss the wall at the
            turning end of the pool)
          </p>
          <h5>
            Backstroke
          </h5>
          <p>
            Like the freestyle, almost anything goes on the
            backstroke as long as you stay on your back. Watching
            swimmers learn the backstroke is a perverse sense of
            fun as they bounce off lane lines and wonder where
            they are. Eventually, they will learn to guide off
            the lane lines, use the overhead backstroke flags and
            the lane line markings to know where they're at in
            the pool, and count strokes from the flags to the
            wall.
          </p>
          <p>
            Backstroke starts are different from all others
            because the swimmer is in the water feet planted
            against the wall, and hanging on to either another
            swimmer's legs or the lip on the pool awaiting the
            starter's signal. "Legs" must be grabbed below the
            knee. Persons serving in an official capacity (such
            as timers or coaches) may not serve as legs. If your
            swimmer backstrokes, he or she will eventually learn
            the backstroke flip turn. This is the one exception
            to staying on your back and can be used only as part
            of a turn (not a finish) at the pool wall.
          </p>
          <h5>
            Breaststroke
          </h5>
          <p>
            The Breaststroke has two components, the kick and the
            arm pull. The pull and its recovery must both be
            under the breast and cannot extend further back than
            the waist area. The kick is a "frog" kick and the
            toes must be pointed outward during the propulsive
            part of the kick. The arm pull and kick must be in an
            alternating sequence and the elbows must stay below
            the water except for tagging the wall at the finish.
            Breaststroke turns and finishes require a
            simultaneous two-hand touch.
          </p>
          <h5>
            Butterfly
          </h5>
          <p>
            A well-executed butterfly (or Fly) is the most
            beautiful exhibition of power you'll ever see in a
            swimming pool. Quite frankly, the fly is the hardest
            stroke for most swimmers to perfect and while they
            are learning it many look like they are drowning.
            There are two components of the fly; the arm pull and
            the kick. The arm pull must be an over the water
            recovery (the arm over the surface of the water from
            elbow to wrist) with the arms moving simultaneously.
            The kick is a dolphin kick with both legs moving
            simultaneously. Unlike the Breaststroke, there is no
            requirement to alternate the kick and pull. Turns and
            finishes require a simultaneous two-hand touch at the
            wall.
          </p>
          <h5>
            Individual Medley
          </h5>
          <p>
            The individual medley (or IM) is when an individual
            swims each of the four strokes in the sequence
            Butterfly, Backstroke, Breaststroke, Freestyle. We
            swim a 100 Meter IM, which means that 25 Meters, or
            one pool length, of each stroke is swum. In a 100
            Meter IM, every turn is a stroke change and stroke
            finish rules apply. This means no Backstroke Flip
            Turns.
          </p>
          <h5>
            Relays
          </h5>
          <p>
            There are two kinds of Relays, the freestyle relay
            and the medley relay. Both involve a team of four
            swimmers, each swimming one quarter of the total
            distance. In the freestyle relay, each swimmer swims
            the freestyle. In the medley relay, the sequence is
            Backstroke, Breaststroke, Butterfly and Freestyle. At
            the NVSL Relay Carnival, swimmers eight and under
            swim a modified medley relay where the fly leg of the
            relay is replaced with a freestyle leg.
          </p>
          <p>
            In all relays, each swimmer must wait until the
            previous swimmer touches the wall prior to leaving
            the deck. Running starts or pushes from teammates are
            not allowed.
          </p>
