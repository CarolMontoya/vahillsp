          <h3>
            <a name="Intro"
                id="Intro"></a>Introduction
          </h3>
          <p>
            Every swimmer's parent has experienced that first day
            of swim practice or that first swim meet where they
            wonder, why am I here and what's going on? We've all
            been there before and will be glad to enlighten you
            in your quest to understand the world of summer
            swimming.
          </p>
          <p>
            Although this information is written for use by
            parents of new Virginia Hills Tidal Wave swimmers,
            the fundamentals are true for any area swim team. It
            will (hopefully) provide some information that will
            help you understand what is happening while your
            child swims. Please remember that the only stupid
            question is the one that is unasked. Your Team Reps,
            Coaches and fellow swim parents all want you to
            understand and enjoy our summer swim program.
          </p>
          <p>
            Welcome to the Tidal Wave &ndash; we're glad to have
            you with us!
          </p>
