          <h5>
            Who Swims?
          </h5>
          <p>
            These meets are to see who can score the most points,
            so the fastest swimmers get to swim. Three swimmers
            can be entered in each individual event and no
            swimmer can swim more than two individual events.
            Since swimmers take vacations and go places such as
            scout camp, and a swimmer can swim in only two events
            (plus relays) in any meet, you don't have to be one
            of the three fastest swimmers to swim in a Saturday
            meet. Who swims an event may seem to be a mystery.
            However, after the first meet both teams know the
            others swimmers times and we try to position our
            swimmers to optimize our points and win.
          </p>
          <h5>
            Disqualifications (DQs) and False Starts
          </h5>
          <p>
            A swimmer will be disqualified (or DQd) if he/she
            does not follow the rules of the stroke or false
            starts. These are covered in more detail in <a href=
            "#ch7">Chapter 7</a>.
          </p>
          <h5>
            Seeding
          </h5>
          <p>
            In Saturday meets, the home team has lanes 1, 3, and
            5 while the visiting team has lanes 2, 4, and 6. The
            fastest swimmers swim in lanes 3 and 4, the next
            fastest in lanes 1 and 2, and the next fastest in
            lanes 5 and 6. Swimmers are seeded based upon their
            fastest times attained in prior competition. Lane 1
            is always on the right side as you stand facing the
            pool at the starting end.
          </p>
          <h5>
            Meet Sheets
          </h5>
          <p>
            While all NVSL meets have an announcer, the best way
            to follow the meet is with a meet sheet, which lists
            all the events, swimmers, and seed times. Meet Sheets
            are usually found in the concession area and
            typically sell for a dollar or two.
          </p>
          <h5>
            Scoring
          </h5>
          <p>
            In the individual events, a first place finish earns
            5 points for the team, a second place 3 points and a
            third place finish 1 point. Relays are scored as 5
            points for the winner and 0 points for the loser.
            There are 402 points up for grabs in a Saturday meet.
            Unless there are one or more places not awarded in an
            event due to DQs or lack of swimmers, you need 202
            points to win.
          </p>
          <p>
            In the event of a tie, the points for the places
            involved are equally split among the swimmers. For
            example, a two-way tie for second place, each swimmer
            earns 2 points (3 points for second plus 1 point for
            third equals 4 points, half for each swimmer). No
            third place would be awarded because the next swimmer
            is fourth. For a third place tie, each swimmer is
            awarded 1/2 point.
          </p>
          <h5>
            Relay Carnivals
          </h5>
          <p>
            In Relay Carnivals, teams are not seeded. Each teams
            lane assignment for the first event is based upon
            luck of the draw and the teams then rotate one lane
            to the left after each event. The meet sheet lists
            only the team swimming in each lane in each event
            (except for All Star Relay Carnival).
          </p>
          <h5>
            Monday Night Developmental Meets B Meets
          </h5>
          <p>
            These meets are designed for all swimmers to improve
            their strokes and times. Although some swimmers are
            not yet ready for A meets, <strong>everyone</strong>
            can swim the B meets.
          </p>
          <p>
            A swimmer who has placed 1<sup>st</sup>,
            2<sup>nd</sup> or 3<sup>rd</sup> in an A meet can no
            longer swim that stroke in the developmental meets
            (unless in an unofficial capacity).
          </p>
          <h5>
            Ribbons
          </h5>
          <p>
            Ribbons are award for 1<sup>st</sup> through
            6<sup>th</sup> Place finishes in all meets.
          </p>
