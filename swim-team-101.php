          <h3>
            <a name="ch6"
                id="ch6"></a>Chapter 6: Who Are All These People
                Dressed in White?
          </h3>
          <p>
            Your first swim meet can be a bewildering experience
            as you encounter a vast horde of adults dressed in
            white tops. NVSL rules require white as the color to
            be worn by all officials. This is also practical as
            white is a good color to wear on a hot humid summer
            morning.
          </p>
          <h5>
            Clerk of the Course
          </h5>
          <p>
            The Clerk of the Course is the "gatekeeper" for all
            swimmers in our meets. The people who perform this
            function get the swimmers to the right lanes for the
            correct race. You can't run a race without swimmers
            and the clerk of the course makes sure the right
            swimmer gets to the right place at the right time.
          </p>
          <h5>
            Referee
          </h5>
          <p>
            The Referee is the chief official for each swim meet.
            He is responsible for the conduct of the meets and is
            the final authority on the interpretation and
            enforcement of all swimming rules. Prior to the start
            of each race, the referee sounds two or three short
            blasts to advise the participants to get ready. After
            the announcer or starter announces the event, the
            Referee sounds one long blast as a signal for
            swimmers to get into position for the start or to
            jump feet first into the water for a backstroke
            event. For Backstroke events, a second long blast is
            given to bring the swimmers to the wall for the
            start. When the referee sees that all the swimmers
            are ready, he extends his arm pointing towards the
            starter. At this point, the starter takes control.
          </p>
          <h5>
            Starter
          </h5>
          <p>
            The Starter is responsible for insuring that all
            swimmers are given a fair and equitable start. The
            starter will instruct the swimmers to "Take your
            mark". After all swimmers are ready and still, the
            starter will start the race, using a "Colorado
            System" (so called because it is built by Colorado
            Timing Systems). This system consists of a public
            address system. a horn, and a strobe light.
          </p>
          <p>
            A race can be recalled only if it was a bad start by
            the starter (i.e. not all the swimmers were ready) or
            for a safety reason. This is done using a recall
            signal on the Colorado system (you'll know it when
            you hear it). For more information, see False Starts
            in <a href="#ch7">Chapter 7</a>.
          </p>
          <h5>
            Stroke and Turn Judges
          </h5>
          <p>
            Once the race has started, the Stroke &amp; Turn
            Judges are responsible for insuring that all swimmers
            obey all the rules for the stroke that they are
            swimming. These people are always at the ends of the
            pool for starts and finishes and walk the sides of
            the pool as best they can within the physical
            constraints of the pool. If a Stroke and Turn Judge
            sees a violation of the rules, he raises his hand to
            signify that an infraction has occurred. A
            Disqualification is recorded on a DQ slip, which the
            referee reviews and approves and forwards copies to
            the Table workers and the Team Rep.
          </p>
          <h5>
            Marshall
          </h5>
          <p>
            Marshals are responsible for ensuring that Warm-ups
            are conducted safely and that order is maintained
            during the warm-ups. Duties include ensuring that
            diving starts are used in warm-ups only when a lane
            is one way away from the starting end, stopping any
            horse play and making sure swimmers aren't hanging or
            sitting on the lane lines.
          </p>
          <h5>
            Relay Take-off Judges
          </h5>
          <p>
            During relays, you'll see four Relay Take-off Judges
            at each end of the pool (two per lane). Their job is
            to ensure that each swimmer touches the wall prior to
            the next swimmer in the relay leaving the deck. Each
            Judge notes on a slip of paper whether each swimmer
            in his lane left before or after the swimmer in the
            water touched the wall. Relay Take-off Judges do not
            raise their hands when they observe an early take-off
            because a disqualification occurs only if both Relay
            Take-off Judges observed an early takeoff.
          </p>
          <h5>
            Timers
          </h5>
          <p>
            The timers are the most important people to every
            swimmer. They are the people who determine each
            swimmer's official time for each race. Being a timer
            is a good entry-level position for new parents to
            help out in. Some parents have been timers for years
            and wouldn't want to see a swim meet from any other
            viewpoint. If you can start and stop a stopwatch, you
            can be a timer. We'll even provide the stopwatch.
            Timers start their watches on the strobe light from
            the Colorado system and stop their watches when the
            swimmer touches the wall. There are three timers per
            lane and all three times are recorded. The middle
            time is the official time. The Chief Timer collects
            the time cards from the timers, reviews them for
            accuracy and completeness, and forwards them on to
            the table workers.
          </p>
          <h5>
            Table Workers
          </h5>
          <p>
            The time cards from the timers and any DQ slips go to
            the Table Workers who determine the order of finish
            for each event, score the meet, and prepare ribbons
            for the participants. Several people from each team
            perform these functions to insure that errors are
            caught before the results are announced.
          </p>
          <h5>
            Team Rep
          </h5>
          <p>
            The Team Rep is the designated recipient of all DQ
            slips for his team and is the only person with any
            official standing to challenge any decisions made by
            the referee. It sounds like an easy job, but
            remember, most of the Team Rep's job is done before
            the meet starts.
          </p>
          <h5>
            Coaches
          </h5>
          <p>
            During the meet, the Coaches primary responsibility
            is to encourage and praise the swimmers and to make
            sure that they get to the Clerk of the Course in time
            to swim.
          </p>
          <h5>
            Other Very Important People
          </h5>
          <p>
            It would be impossible to host a swim meet without a
            number of people in Other Very Important Positions.
            These people run telephone trees, edit newsletters,
            set up the pool and sell concessions. They also
            announce the results, run social activities, act as
            Marshals in the team area and do other jobs that need
            to be done. <strong>We need the help of every family
            in order to have a successful swim season</strong>.
          </p>
